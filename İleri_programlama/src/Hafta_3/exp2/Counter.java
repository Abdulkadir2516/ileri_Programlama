package Hafta_3.exp2;

/**
 *
 * @author recep
 */
public class Counter {

    private long c;

    public Counter() {
        c = 0;
    }

    public void increment() {
        c++;
    }

    public void decrement() {
        c--;
    }

    public long value() {
        return c;
    }
    
}
