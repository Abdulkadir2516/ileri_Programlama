
package Hafta_3.exp3;

/**
 * @author abdul
 */
public class Food {
    private final String name;
    private final int  time;
    
    public Food(String name, int time)
    {
        this.name = name;
        this.time = time;
        
    }
    public String getName()
    {
        return name;
    }
    
    public int getTime()
    {
        return time;
    }
    
}
